  },
  methods: {
    switchTab(tab) {
      if(tab === this.currentTab) return;
      this.currentTab = tab;
      let url = '';
      if(tab==='home') url = '/pages/home/home';
      else if(tab==='index') url = '/pages/index/index';
      else if(tab==='focus') url = '/pages/focus/focus';
